public class Direction {
    private String from;
    private String to;

    public Direction(String from, String to) {
        this.from = from;
        this.to = to;
    }

    // Getters and Setters (you can generate these using your IDE)

    public String getFrom() {
        return from;
    }

    public String getTo() {
        return to;
    }
}
